function shuffle<T>(items: T[]): T[] {
  const copy = [...items];
  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

function normalizeCustomWords(customWords?: string[]): DrawingWord[] {
  if (!customWords || customWords.length === 0) return [];
  return customWords.map((word, index) => ({
    id: `custom-${index}-${word.toLowerCase().replace(/\s+/g, '-')}`,
    word,
    category: 'Custom',
    difficulty: 'easy',
  }));
}

export function buildWordQueue(totalWords: number, customWords?: string[]): DrawingWord[] {
  const customList = normalizeCustomWords(customWords);
  const basePool = [...DRAWING_WORDS, ...customList];
  if (basePool.length === 0 || totalWords <= 0) return [];

  const queue: DrawingWord[] = [];
  let pool = shuffle(basePool);

  while (queue.length < totalWords) {
    if (pool.length === 0) {
      pool = shuffle(basePool);
    }
    const next = pool.shift();
    if (next) queue.push(next);
  }

  return queue;
}

export function parseCustomWords(input: string): string[] {
  return input
    .split(/\r?\n|,/)
    .map(word => word.trim())
    .filter(Boolean);
}
